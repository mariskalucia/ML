# Vragen wat de graden celsius is
Celsius = float(input("Wat is de graden Celsius? "))
# De berekening van celsius naar fahrenheit
Fahrenheit = 32 + (Celsius*1.8)
# Het printen van het antwoord
print ("Fahrenheit: " + str(Fahrenheit))

# Vragen wat de graden fahrenheit is
Fahrenheit = float(input("Wat is de graden Fahrenheit? "))
# De berekening van fahrenheit naar celsius
Celsius = (Fahrenheit - 32) / 1.8
# Het printen van het antwoord
print ("Celsius: " + str(Celsius)) 


 