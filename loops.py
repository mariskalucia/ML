# Loops
print("Loops")

for x in range(0, 11, 2):
    print(x)

for x in range(10, -1, -1):
    print(x)

title = "Good Die Young"
print(title[0])

for index in range (0, len(title)):
    print(title[index])

for index in range (len(title)-1, -1, -1):
    print(title[index])

for letter in title:
    print(letter)

